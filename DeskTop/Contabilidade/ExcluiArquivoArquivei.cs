        private void empresaComboBoxAdv_Validating(object sender, CancelEventArgs e)
        {
            ((ComboBoxAdv)sender).FlatBorderColor = Publicas._bordaSaida;

            if (Publicas._escTeclado)
            {
                Publicas._escTeclado = false;
                return;
            }

            foreach (var item in _listaEmpresas.Where(w => w.CodigoeNome == empresaComboBoxAdv.Text))
            {
                _empresa = item;
            }
        }

        private void inicialDateTimePicker_Validating(object sender, CancelEventArgs e)
        {
            ((DateTimePickerAdv)sender).BorderColor = Publicas._bordaSaida;

            if (Publicas._escTeclado)
            {
                Publicas._escTeclado = false;
                return;
            }
            
            finalDateTimePicker.Value = inicialDateTimePicker.Value;
        }

        private void finalDateTimePicker_Validating(object sender, CancelEventArgs e)
        {
            if (Publicas._escTeclado)
            {
                Publicas._escTeclado = false;
                return;
            }

            ((DateTimePickerAdv)sender).BorderColor = Publicas._bordaSaida;

            if (finalDateTimePicker.Value.Date < inicialDateTimePicker.Value.Date)
            {
                new Notificacoes.Mensagem("Data final não pode ser menor que a data inicial.", Publicas.TipoMensagem.Alerta).ShowDialog();
                inicialDateTimePicker.Focus();
                return;
            }
        }

        private void limparButton_Validating(object sender, CancelEventArgs e)
        {
            limparButton.BackColor = Publicas._botao;
            limparButton.ForeColor = Publicas._fonteBotao;
        }

        private void excluirButton_Validating(object sender, CancelEventArgs e)
        {
            excluirButton.BackColor = Publicas._botao;
            excluirButton.ForeColor = Publicas._fonteBotao;
        }

        private void pesquisarButton_Click(object sender, EventArgs e)
        {
            gridGroupingControl.DataSource = new List<Classes.ImportandoArquivei>();
            _lista = new ArquiveiBO().ListarArquivosImportador(_empresa.IdEmpresa, inicialDateTimePicker.Value, finalDateTimePicker.Value);

            gridGroupingControl.DataSource = _lista;
            excluirButton.Enabled = _lista.Count() != 0;

        }

        private void powerPictureBox_Click(object sender, EventArgs e)
        {
            Close();
        }

        private void limparButton_Click(object sender, EventArgs e)
        {
            gridGroupingControl.DataSource = new List<Classes.ImportandoArquivei>();
            empresaComboBoxAdv.Focus();
        }

        private void excluirButton_Click(object sender, EventArgs e)
        {
            if (new Notificacoes.Mensagem("Confirma a exclusão dos Arquivos selecionados ? " , Publicas.TipoMensagem.Confirmacao).ShowDialog() == DialogResult.No)
                return;

            foreach (var item in _lista.Where(w => w.Importando))
            {
                if (!new ArquiveiBO().ExcluirArquivosImportandos(item.Arquivo))
                {
                    new Notificacoes.Mensagem("Problemas durante a exclusão." + Environment.NewLine + Publicas.mensagemDeErro, Publicas.TipoMensagem.Erro).ShowDialog();
                    return;
                }
            }

            new Notificacoes.Mensagem("Exclusão efetuada com sucesso.", Publicas.TipoMensagem.Sucesso).ShowDialog();
        }
