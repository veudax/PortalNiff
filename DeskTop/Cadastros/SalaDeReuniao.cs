        private void codigoTextBox_KeyPress(object sender, KeyPressEventArgs e)
        {
            if (!char.IsControl(e.KeyChar) && !char.IsDigit(e.KeyChar))
            {
                e.Handled = true;
            }

            if (e.KeyChar == '+')
            {
                codigoTextBox.Text = string.Empty;
                proximoButton_Click(sender, e);
            }
        }

        private void proximoButton_Click(object sender, EventArgs e)
        {
            codigoTextBox.Text = new SalaDeReuniaoBO().Proximo().ToString();
            ativoCheckBox.Focus();
        }

        private void codigoTextBox_Validating(object sender, CancelEventArgs e)
        {
            if (Publicas._escTeclado)
            {
                Publicas._escTeclado = false;
                return;
            }

            if (codigoTextBox.Text.Trim() == "")
            {
                new Pesquisas.SalaDeReuniao().ShowDialog();

                codigoTextBox.Text = Publicas._idRetornoPesquisa.ToString();

                if (codigoTextBox.Text.Trim() == "" || codigoTextBox.Text == "0")
                {
                    codigoTextBox.Text = string.Empty;
                    codigoTextBox.Focus();
                    return;
                }
            }

            _sala = new SalaDeReuniaoBO().Consultar(Convert.ToInt32(codigoTextBox.Text));

            if (_sala != null)
            {
                nomeTextBox.Text = _sala.Descricao;
                ativoCheckBox.Checked = _sala.Ativo;
                capacidadeCurrencyTextBox.Text = _sala.Capacidade.ToString();

                for (int i = 0; i < empresaComboBoxAdv.Items.Count; i++)
                {
                    empresaComboBoxAdv.SelectedIndex = i;
                    if (empresaComboBoxAdv.Text == _sala.Nome)
                        break;
                }
            }

            gravarButton.Enabled = true;
            excluirButton.Enabled = _sala.Existe;
            ((TextBoxExt)sender).BorderColor = Publicas._bordaSaida;

            if (Publicas._idRetornoPesquisa != 0)
                ativoCheckBox.Focus();
        }

        private void nomeTextBox_Validating(object sender, CancelEventArgs e)
        {
            ((TextBoxExt)sender).BorderColor = Publicas._bordaSaida;
        }

